# Variáveis Numéricas:

# int: Representa números inteiros.


idade = 25



#float: Representa números de ponto flutuante (números decimais).


altura = 1.75

# Variáveis de Texto:

# str: Representa sequências de caracteres, texto.


nome = "João"

# Variáveis Booleanas:

# bool: Representa valores booleanos True ou False.

tem_carro = True

#Listas:

# list: Representa uma coleção ordenada de itens que podem ser modificados.


numeros = [1, 2, 3, 4, 5]

# Tuplas:

# tuple: Representa uma coleção ordenada de itens que não podem ser modificados.

coordenadas = (10, 20)

# Dicionários:

# dict: Representa uma coleção de pares chave-valor.


pessoa = {'nome': 'Maria', 'idade': 30, 'cidade': 'São Paulo'}

# Conjuntos:

# set: Representa uma coleção de elementos únicos e não ordenados.


numeros_primos = {2, 3, 5, 7, 11}

# Variáveis Nulas:

# NoneType: Representa a ausência de valor.


valor = None